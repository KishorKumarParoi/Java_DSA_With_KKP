public class primitives {
    public static void main(String[] args) {
        /// data type which can't be broken into smaller part is called primitive data
        /// types
        int roll = 23;
        char letter = 'k';
        long longIntger = 132432235L;
        double dd = 45.34;
        float f = 232.45F;

        System.out.println(roll);
        System.out.println(letter);
        System.out.println(longIntger);
        System.out.println(dd);
        System.out.println(f);
        System.out.println(dd);
        // rapper class;
        Integer rno = 34;
        System.out.println(rno);
    }
}
